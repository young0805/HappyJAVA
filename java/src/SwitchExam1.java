public class SwitchExam1 {
    public static void main(String[] args) {
        int num = 3;
        switch(num){
            case 1 :
                System.out.println("1입니다");
            case 2 :
                System.out.println("2입니다");
            case 3 :
                System.out.println("3입니다");
        }
    }
}
